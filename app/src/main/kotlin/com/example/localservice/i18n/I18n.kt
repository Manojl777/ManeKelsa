fun convertToKannadaNumerals(numberString: String): String {
    val kannadaNumerals = arrayOf("೦", "೧", "೨", "೩", "೪", "೫", "೬", "೭", "೮", "೯")
    var result = numberString
    for (i in 0..9) {
        result = result.replace(i.toString(), kannadaNumerals[i])
    }
    return result
}

